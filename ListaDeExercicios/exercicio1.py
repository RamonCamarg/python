# Declarando as variáveis a e b e atribuindo valores
a = 10
b = 3

# Realizando as operações
soma = a + b
subtracao = a - b
multiplicacao = a * b
divisao = a / b
divisao_inteira = a // b
resto_divisao = a % b
potenciacao = a ** b

# Imprimindo os resultados das operações
print("Soma:", soma)
print("Subtração:", subtracao)
print("Multiplicação:", multiplicacao)
print("Divisão:", divisao)
print("Divisão Inteira:", divisao_inteira)
print("Resto da Divisão:", resto_divisao)
print("Potenciação:", potenciacao)

# Declarando o raio de um círculo e calculando a área
raio = 5
area = 3.14 * raio ** 2

# Imprimindo o resultado da área
print("Área do círculo com raio", raio, "é:", area)
