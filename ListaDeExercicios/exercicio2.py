# Declarando as strings nome e sobrenome e concatenando-as
nome = "João"
sobrenome = "Silva"
nome_completo = nome + " " + sobrenome

# Imprimindo o nome completo
print("Nome completo:", nome_completo)

# Declarando uma frase
frase = "Python é uma linguagem de programação poderosa."

# Convertendo todas as letras para maiúsculas
frase_maiusculas = frase.upper()

# Convertendo todas as letras para minúsculas
frase_minusculas = frase.lower()

# Substituindo uma palavra por outra na frase
nova_frase = frase.replace("poderosa", "incrível")

# Imprimindo os resultados
print("Frase em maiúsculas:", frase_maiusculas)
print("Frase em minúsculas:", frase_minusculas)
print("Frase com substituição de palavra:", nova_frase)
